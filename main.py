    def draw_dots(self):
        for column_number in range(self.grid.shape[1]):
            for line_number in range(self.grid.shape[0]):
                if self.grid[line_number, column_number] == 2:
                    pyxel.rect(column_number * 8 + 3, line_number * 8 + 3, 2, 2, 2)
                elif self.grid[line_number, column_number] == 3:
                    pyxel.blt(column_number * 8, line_number * 8, 0, 224, 0, 8, 8, 0)
