direction_offset = {
    0: (0, 1),  # Down
    1: (0, -1),  # Up
    2: (-1, 0),  # Left
    3: (1, 0)  # Right
}


def draw_dots():
    for column_number in range(grid.shape[1]):
        for line_number in range(grid.shape[0]):
            if grid[line_number, column_number] == 2:
                pyxel.rect(column_number * 8 + 3, line_number * 8 + 3, 2, 2, 2)
            elif grid[line_number, column_number] == 3:
                pyxel.blt(column_number * 8, line_number * 8, 0, 224, 0, 8, 8, 0)
