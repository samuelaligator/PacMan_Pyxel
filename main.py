class Pacman:

    def __init__(self):
        self.x = 104  # 106 position de depart
        self.y = 184
        self.x_grid = 13
        self.y_grid = 23
        self.input_direction = 2
        self.direction = 2
        self.walking = True

    def draw_pacman(self):
        pyxel.blt(self.x - 2, self.y - 3, 1, 35, 1, 13, 13, 0)

    def pacman_direction(self):
        direction = {0: pyxel.KEY_DOWN, 1: pyxel.KEY_UP, 2: pyxel.KEY_LEFT, 3: pyxel.KEY_RIGHT}
        for i in range(4):
            if pyxel.btn(direction[i]):
                self.input_direction = i

    def pacman_movement(self):

        if self.x % 8 == 0 and self.y % 8 == 0:
            self.x_grid = self.x // 8
            self.y_grid = self.y // 8
            self.check_walls()

        if self.walking:
            offset_x, offset_y = direction_offset[self.direction]
            self.x += offset_x
            self.y += offset_y

    def check_walls(self):
        x_offset, y_offset = direction_offset[self.input_direction]
        x_offset2, y_offset2 = direction_offset[self.direction]

        if grid[self.y_grid + y_offset, self.x_grid + x_offset] != 1:
            self.direction = self.input_direction
            self.walking = True
        elif grid[self.y_grid + y_offset2, self.x_grid + x_offset2] == 1:
            self.walking = False


class App:
    def __init__(self):
        pyxel.init(224, 248, "Pac-Man", 60, pyxel.KEY_ESCAPE, 10)
        pyxel.load("theme.pyxres")
        self.pacman = Pacman()
        pyxel.run(self.update, self.draw)

    def update(self):
        self.pacman.pacman_direction()
        self.pacman.pacman_movement()

    def draw(self):
        pyxel.cls(0)
        draw_dots()
        pyxel.blt(0, 0, 0, 0, 0, 224, 248, 0)
        self.pacman.draw_pacman()


App()
